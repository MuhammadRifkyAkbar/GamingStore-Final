    private void tabelMLMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMLMouseClicked
    tabelML.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
        @Override
        public void valueChanged(ListSelectionEvent event) {
            if (!event.getValueIsAdjusting() && tabelML.getSelectedRow() != -1) {
                // Get the selected row index
                int selectedRowIndex = tabelML.getSelectedRow();

                // Assuming "IDGame" is at index 2
                Long idGame = (Long) tabelML.getValueAt(selectedRowIndex, 2);

                // Assuming you have fields to display the data
                // For example, you have JTextFields named txtIDGame, txtNamaGame, txtHargaGame
                txtIDGame.setText(String.valueOf(idGame));

                // Fetch the corresponding ItemGame from the database
                DAO_ItemGame daoItemGame = new DAO_ItemGame();
                ItemGame selectedGame = daoItemGame.getItemGameByID(idGame);

                // Display the data in the text fields
                if (selectedGame != null) {
                    txtNamaGame.setText(selectedGame.getNama_game());
                    txtHargaGame.setText(String.valueOf(selectedGame.getHarga_game()));
                }
            }
        }
    });    // TODO add your handling code here:
    }//GEN-LAST:event_tabelMLMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
        // Get the updated values from the text fields
        Long idGame = Long.parseLong(txtIDGame.getText());
        Long hargaGame = Long.parseLong(txtHargaGame.getText());
        String namaGame = txtNamaGame.getText();

        // Create an ItemGame object with the updated values
        ItemGame updatedItemGame = new ItemGame();
        updatedItemGame.setIDGame(idGame);
        updatedItemGame.setHarga_game(hargaGame);
        updatedItemGame.setNama_game(namaGame);

        // Update the ItemGame in the database
        DAO_ItemGame daoItemGame = new DAO_ItemGame();
        daoItemGame.updateItemGame(updatedItemGame);

        // Refresh the table to reflect the changes
        tampilBarang();

        // Display a success message
        JOptionPane.showMessageDialog(this, "Item Game updated successfully");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Failed to update item game. Please ensure that input values are correct.");
        // Handle the exception appropriately, e.g., log it
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage());
        // Handle the exception appropriately, e.g., log it
    }
    }//GEN-LAST:event_jButton1ActionPerformed

