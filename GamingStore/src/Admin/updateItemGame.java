private void tampilBarang() {
    // Mendapatkan semua itemGame dari database
    DAO_ItemGame daoItemGame = new DAO_ItemGame();
    List<ItemGame> itemList = daoItemGame.getAllItemGames();

    // Membuat array untuk menyimpan kolom tabel
    Object[] baris = {"Harga", "Nama Game", "ID Game"};

    // Membuat model tabel dengan array kolom
    DefaultTableModel modelTabel = new DefaultTableModel(null, baris);

    // Menambahkan data itemGame ke model tabel
    for (ItemGame itemGame : itemList) {
        Object[] data = {
            itemGame.getHarga_game(),
            itemGame.getNama_game(),
            itemGame.getIDGame()
        };
        modelTabel.addRow(data);
    }

    // Mengatur model tabel pada tabelML
    tabelML.setModel(modelTabel);
}

