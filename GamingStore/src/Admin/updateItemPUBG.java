    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    // Get the updated values from the text fields
    Long idBarang = Long.parseLong(txtIDbarang.getText());
    Long harga = Long.parseLong(txtHarga.getText());
    String nickname = txtNickname.getText();
    int weapon = Integer.parseInt(txtSkin.getText());
    int kostum = Integer.parseInt(txtHero.getText());
    String charac = txtLoginAkun.getText();

    itemPUBG updatedItemPUBG = new itemPUBG();
    updatedItemPUBG.setIDbar(idBarang);
    updatedItemPUBG.setHarga(harga);
    updatedItemPUBG.setNickname(nickname);
    updatedItemPUBG.setSenjata(weapon);
    updatedItemPUBG.setVault(kostum);  
    updatedItemPUBG.setCharac(charac);

    // Update the itemPUBG in the database
    DAO_ItemPUBG daoItemPUBG = new DAO_ItemPUBG();
    daoItemPUBG.updateItem(updatedItemPUBG);


    tampilBarangPUBG();
    }//GEN-LAST:event_jButton1ActionPerformed

