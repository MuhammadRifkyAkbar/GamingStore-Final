private void tampilBarangPUBG() {
    // Mendapatkan semua itemPUBG dari database
    DAO_ItemPUBG daoItemPUBG = new DAO_ItemPUBG();
    List<itemPUBG> itemListPUBG = daoItemPUBG.getAllItems();

    // Membuat array untuk menyimpan kolom tabel
    Object[] kolom = {"IDBarang", "Harga", "Nickname", "Senjata", "Vault", "Charac"};

    // Membuat model tabel dengan array kolom
    DefaultTableModel modelTabel = new DefaultTableModel(null, kolom);

    // Menambahkan data itemPUBG ke model tabel
    for (itemPUBG itemPUBG : itemListPUBG) {
        Object[] data = {
            itemPUBG.getIDbar(),
            itemPUBG.getHarga(),
            itemPUBG.getNickname(),
            itemPUBG.getSenjata(),
            itemPUBG.getVault(),
            itemPUBG.getCharac()
        };
        modelTabel.addRow(data);
    }

    // Mengatur model tabel pada tabelPUBG
    tabelPUBG.setModel(modelTabel);
}

private void setFieldsFromSelectedRow(JTable table, JTextField txtIDbarang, JTextField txtHarga, JTextField txtNickname, JTextField txtSkin, JTextField txtHero, JTextField txtLoginAkun) {
    int selectedRowIndex = table.getSelectedRow();
    if (selectedRowIndex != -1) {
        // Assuming "IDBarang" is at index 0
        Long idBarang = (Long) table.getValueAt(selectedRowIndex, 0);

        // Assuming you have fields to display the data
        // For example, you have JTextFields named txtIDBarang, txtHarga, txtNickname, txtSkin, txtHero, txtLoginAkun
        txtIDbarang.setText(String.valueOf(idBarang));
        txtHarga.setText(String.valueOf(table.getValueAt(selectedRowIndex, 1)));
        txtNickname.setText(String.valueOf(table.getValueAt(selectedRowIndex, 2)));
        txtSkin.setText(String.valueOf(table.getValueAt(selectedRowIndex, 3)));
        txtHero.setText(String.valueOf(table.getValueAt(selectedRowIndex, 4)));
        txtLoginAkun.setText(String.valueOf(table.getValueAt(selectedRowIndex, 5)));
    }
}

