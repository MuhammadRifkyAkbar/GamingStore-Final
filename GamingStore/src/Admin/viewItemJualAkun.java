private void tampilBarang() {
    // Mendapatkan semua beliAkun dari database
    DAO_BeliAkun daoBeliAkun = new DAO_BeliAkun();
    List<Model.BeliAkun> beliAkunList = daoBeliAkun.getAllBeliakunMLs();

    // Membuat array untuk menyimpan kolom tabel
    Object[] baris = {"Nickname", "Harga", "PPN", "Income", "Email"};

    // Membuat model tabel dengan array kolom
    DefaultTableModel modelTabel = new DefaultTableModel(null, baris);

    // Menambahkan data beliAkun ke model tabel
    for (Model.BeliAkun beliAkun : beliAkunList) {
        Object[] data = {
            beliAkun.getNick(),
            beliAkun.getHarga(),
            beliAkun.getPpn(),
            beliAkun.getIncome(),
            beliAkun.getEmail()
        };
        modelTabel.addRow(data);
    }

    // Mengatur model tabel pada tabelML
    tabelML.setModel(modelTabel);
}

